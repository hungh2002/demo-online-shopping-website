package com.hungh2002.service.utils;

/**
 * GetUrlParam
 */
public class UrlParam {

    public static String GetUrlParam(String urlParam) {
        String urlParamString = "";
        if (urlParam == null) {
            urlParamString = "";
        } else {
            urlParamString = urlParam;
        }
        return urlParamString;
    }
}
